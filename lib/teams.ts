// Standard NCAA bracket seeding order (which seed plays which seed position in each "pod")
// Order: 1,16,8,9,5,12,4,13,6,11,3,14,7,10,2,15
export const SEED_ORDER = [1, 16, 8, 9, 5, 12, 4, 13, 6, 11, 3, 14, 7, 10, 2, 15];

export function getTeamsByRegion(region: string): Team[] {
  const order = SEED_ORDER;
  const regionTeams = TEAMS.filter(t => t.region === region);
  return order
    .map(seed => regionTeams.find(t => t.seed === seed))
    .filter(Boolean) as Team[];
}

export function getTeamById(id: string): Team | undefined {
  return TEAMS.find(t => t.id === id);
}
